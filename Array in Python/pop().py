#pop() method in python array

import array 
            #1 = 20
numArr = array.array('i',[10,20,30,40,50])
print("array items are:")
print(numArr)


#deleting the item at index 1

numArr.pop(1)
print("\n array items after reversing the array: ")
print(numArr)

numArr.pop()
print("\n array items after reversing the array: ")
print(numArr)
